                    $(document).ready(function() {
                        $.fn.dataTableExt.oApi.fnPagingInfo = function(oSettings)
                        {
                            return {
                                "iStart": oSettings._iDisplayStart,
                                "iEnd": oSettings.fnDisplayEnd(),
                                "iLength": oSettings._iDisplayLength,
                                "iTotal": oSettings.fnRecordsTotal(),
                                "iFilteredTotal": oSettings.fnRecordsDisplay(),
                                "iPage": Math.ceil(oSettings._iDisplayStart / oSettings._iDisplayLength),
                                "iTotalPages": Math.ceil(oSettings.fnRecordsDisplay() / oSettings._iDisplayLength)
                            };
                        };

                        var t = $("#mytable").dataTable({
                            initComplete: function() {
                                var api = this.api();
                                $('#mytable_filter input')
                                        .off('.DT')
                                        .on('keyup.DT', function(e) {
                                            if (e.keyCode == 13) {
                                                api.search(this.value).draw();
                                    }
                                });
                            },
                            oLanguage: {
                                sProcessing: "loading..."
                            },
                            processing: true,
                            serverSide: true,
                            ajax: {"url": "<?php echo base_url() ?>member_retail/json", "type": "POST"},
                            columns: [
                                {
                                    "data": "id_member",
                                    "orderable": false
                                },{"data": "id_member", "orderable": false},{"data": "kode"},{"data": "nama"},{"data": "alamat"},{"data": "telp"},{"data": "tgl_lahir"},{"data": "diskon"},
                            ],
                            order: [[0, 'desc']],
                            rowCallback: function(row, data, iDisplayIndex) {
                                var info = this.fnPagingInfo();
                                var page = info.iPage;
                                var length = info.iLength;
                                var index = page * length + (iDisplayIndex + 1);
                                $('td:eq(0)', row).html(index);
                                $('td:eq(1)', row).html('<center><div class="checkbox"><label><input name="list_cetak[]" id="list_cetak" type="checkbox" class="ace" value="'+data.id_member+'" /><span class="lbl"></span></label></div></center>');
                            }
                        });

                        function cek_chk() {
                          var chk = $("#chk_cetak").is(':checked');
                          return chk;
                        }

                        $("#chk_cetak").on('change', function(e){
                          if (cek_chk()) {
                            $("input[id*=list_cetak]").prop('checked', true);
                          } else {
                            $("input[id*=list_cetak]").prop('checked', false);
                          }
                        });

                    });
